bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
        case QWERTY:
          if (record->event.pressed) {
            layer_off(_BWPO_FOR_QWERTY);
            layer_off(_BWPO_FOR_AZERTY);
            layer_off(_BWPO_FOR_BEPO);
            layer_off(_BWPO_FOR_CUSTOM);
          }
          return false;
          break;
        case QWERTY_S:
          if (record->event.pressed) {
            layer_invert(_QWERTY_SYMBOLS);
          }
          return false;
          break;
        case BWPO_FQ:
          if (record->event.pressed) {
            layer_on(_BWPO_FOR_QWERTY);
            layer_off(_BWPO_FOR_AZERTY);
            layer_off(_BWPO_FOR_BEPO);
            layer_off(_BWPO_FOR_CUSTOM);
          }
          return false;
          break;
        case BWPO_FA:
          if (record->event.pressed) {
            layer_off(_BWPO_FOR_QWERTY);
            layer_on(_BWPO_FOR_AZERTY);
            layer_off(_BWPO_FOR_BEPO);
            layer_off(_BWPO_FOR_CUSTOM);
          }
          return false;
          break;
        case BWPO_FB:
          if (record->event.pressed) {
            layer_off(_BWPO_FOR_QWERTY);
            layer_off(_BWPO_FOR_AZERTY);
            layer_on(_BWPO_FOR_BEPO);
            layer_off(_BWPO_FOR_CUSTOM);
          }
          return false;
          break;
        case BWPO_FC:
          if (record->event.pressed) {
            layer_off(_BWPO_FOR_QWERTY);
            layer_off(_BWPO_FOR_AZERTY);
            layer_off(_BWPO_FOR_BEPO);
            layer_on(_BWPO_FOR_CUSTOM);
          }
          return false;
          break;
        case LOWER:
          if (record->event.pressed) {
            layer_on(_LOWER);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
          } else {
            layer_off(_LOWER);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
          }
          return false;
          break;
        case RAISE:
          if (record->event.pressed) {
            layer_on(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
          } else {
            layer_off(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
          }
          return false;
          break;
      }
    return true;
};
